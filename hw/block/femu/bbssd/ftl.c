/**
 * @brief 空闲gc阈值
 * 
 * @param ssd 
 * @return true 
 * @return false 
 */
static inline bool should_gc(struct ssd *ssd)
{
    return (ssd->lm.free_line_cnt <= ssd->sp.gc_thres_lines);
    //lm 是用于 FTL 的空闲 line 链表，sp 是 ssd 参数，gc_thres_lines是gc阈值参数
}

